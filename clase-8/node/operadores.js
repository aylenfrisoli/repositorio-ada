//Operadores aritméticos
let a = 10;
let b = 2;

console.log(a + b); //Suma = 12
console.log(a - b); //Resta = 8
console.log(a * b); //Multiplicación = 20
console.log(a / b); //División = 5
console.log(a % b); //Módulo = 0

//Operadores de comparación
console.log(a > b); //Mayor que = true
console.log(a < b); //Menor que = false
console.log(a === "10"); //Igualdad estricta = false falso porque a es numero y "10" es texto

//Operadores lógicos
console.log(true && false); //AND: true porque ambos son verdaderos
