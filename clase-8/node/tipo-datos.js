//Definimos diferentes tipos de datos

let numero = 29; //número
let texto = "Hola Mundo"; //cadena de texto
let booleano = true; //boolenano (verdadero/falso)
let indefinido; //variable no inicianlizada
let nulo = null; //valor nulo
let objeto = { nombre: "Aylén", edad: 29 }; //objeto con propiedades

//Mostrar los valores y explicar su tipo

console.log(`Número`, numero); //esto es un número
console.log(`Texto`, texto); //esto es un texto
console.log(`Booleano`, booleano); //esto es un booleano
console.log(`Indefinido`, indefinido); //esto es indefinido (variable sin valor)
console.log(`Nulo`, nulo); //esto es nulo (valor vacío )
console.log(`Objeto`, objeto); //esto es un objeto (con nombre y edad)
